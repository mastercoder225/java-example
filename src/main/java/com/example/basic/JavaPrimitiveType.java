package com.example.basic;

public class JavaPrimitiveType {

    public static void main(String[] args) {
        byte    a1; // SIZE=8  BYTES=1 DEFAULT=0  MIN=-128 MAX=127
        short   a2; // SIZE=16 BYTES=2 DEFAULT=0  MIN=-32768 MAX=32767
        int     a3; // SIZE=32 BYTES=4 DEFAULT=0  MIN=-2147483648 MAX=2147483647
        long    a4; // SIZE=64 BYTES=8 DEFAULT=0L MIN=-9223372036854775808 MAX=9223372036854775807
        float   a5; // SIZE=32 BYTES=4 DEFAULT=0.0F MIN= MAX=
        double  a6; // SIZE=64 BYTES=8 DEFAULT=0.0  MIN= MAX=
        char    a7; // SIZE=16 BYTES=2 DEFAULT='\u0000' MIN='\u0000' MAX='\uffff'
        boolean a8; // VALUE_RANGE=(true|false) DEFAULT=false
    }
}
